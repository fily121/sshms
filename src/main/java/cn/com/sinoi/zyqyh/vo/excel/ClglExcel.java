package cn.com.sinoi.zyqyh.vo.excel;

/**
 * 材料管理页面的领取记录导出用的类。
 *
 * @author LDNS 杜保軍
 */
public class ClglExcel {

    private String clmc;
    private String detail;
    private String lqtime;
    private String lqsl;
    private String sysl;
    private String clshengyu;
    private String sgdmc;

    public String getClmc() {
        return clmc;
    }

    public void setClmc(String clmc) {
        this.clmc = clmc;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getLqtime() {
        return lqtime;
    }

    public void setLqtime(String lqtime) {
        this.lqtime = lqtime;
    }

    public String getLqsl() {
        return lqsl;
    }

    public void setLqsl(String lqsl) {
        this.lqsl = lqsl;
    }

    public String getSysl() {
        return sysl;
    }

    public void setSysl(String sysl) {
        this.sysl = sysl;
    }

    public String getClshengyu() {
        return clshengyu;
    }

    public void setClshengyu(String clshengyu) {
        this.clshengyu = clshengyu;
    }

    public String getSgdmc() {
        return sgdmc;
    }

    public void setSgdmc(String sgdmc) {
        this.sgdmc = sgdmc;
    }
}
